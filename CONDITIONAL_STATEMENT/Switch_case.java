package CONDITIONAL_STATEMENT;

public class Switch_case {
    public static void main(String[] args) {
        int number = 2;
        switch(number){
            case 1:
            System.out.println("samoshe");
            case 2:
            System.out.println("momos");
            break;
            case 3:
            System.out.println("burger");
            default :
            System.out.println("wake up morning");
        }
    }
    
}
    
